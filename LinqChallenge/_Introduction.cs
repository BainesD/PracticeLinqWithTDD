    #region How Do You Use This Repository?    
    /*     
          
            This depends upon your goal. This repository is set up to support users with three goals in mind.


            1) If your goal is only to practice Linq - Use the "Strictly_Linq" branch

                    This branch will have tests pre-created for each method.
                    This branch does not focus on TDD - so all tests will be uncommented from the start.


            2) If your goal is to practice Linq with a minor focus on TDD - Use the "Tests_PreMade" branch 

                    This branch will have (most) tests already created for you. Once you pass a test, 
                    you will uncomment the next one until you meet all requirements. This is a great
                    start for TDD, since the idea with Test Driven Development is for you to only 
                    write enough code to pass the current test that you are working on.


            3) If your goal is to practice Test Driven Development - Use the "TDD_Practice" branch

                    This branch will provide you with a list of requirements that you can use to create
                    your tests. If that seems intimidating, remember that you can reference the other
                    branches for ideas. Your requirements will be broken down into simple bullet points
                    that are "One Requirement => One Test".    

                           --------------------------------------------------------------------------
                           |    When using this branch, please remember to practice TDD properly.   |
                           |    Create one tests at a time, run it first to ensure it fails.        |
                           |    After passing one test, create the next one. One at a time.         |
                           --------------------------------------------------------------------------            
    */
